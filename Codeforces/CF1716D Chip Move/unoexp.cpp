    f[0][0] = 1;

    for (int i = 1; i < 655; ++i) {
        if (i > 1) f[0][0] = 0;
        int k = c + i - 1;
        for (int j = 1; j <= n ; ++j) {
            ff[j] = 0;
            if (j >= k) {
                ff[j] = f[j - k][!(i & 1)] + ff[j - k];
            }
            f[j][i & 1] = ff[j];
            ans[j] += ff[j];
        }
    }

    for (int i = 1; i <= n; ++i) {
        cout << ans[i].val() << " ";
    }

        
